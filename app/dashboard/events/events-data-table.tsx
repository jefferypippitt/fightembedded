// Helper component for the actions cell
const ActionsCell = ({ event }: { event: UFCEvent }) => {
  const router = useRouter()

  const handleDelete = async () => {
    try {
      const result = await deleteEvent(event.id)
      if (result.status === "success") {
        toast.success("Event deleted successfully")
        router.refresh()
      } else {
        toast.error(result.message || "Failed to delete event")
      }
    } catch (error) {
      toast.error(
        error instanceof Error
          ? `Error: ${error.message}`
          : "An unexpected error occurred. Please try again."
      )
    }
  }

  return (
    <DropdownMenu>
      <DropdownMenuTrigger asChild>
        <Button
          variant="ghost"
          className="data-[state=open]:bg-muted text-muted-foreground flex size-8"
          size="icon"
        >
          <MoreHorizontal className="h-4 w-4" />
          <span className="sr-only">Open menu</span>
        </Button>
      </DropdownMenuTrigger>
      <DropdownMenuContent align="end" className="w-32">
        <DropdownMenuItem asChild>
          <Link href={`/dashboard/events/${event.id}/edit`}>
            Edit
          </Link>
        </DropdownMenuItem>
        <DropdownMenuSeparator />
        <DropdownMenuItem onClick={handleDelete} className="text-red-600">
          Delete
        </DropdownMenuItem>
      </DropdownMenuContent>
    </DropdownMenu>
  )
}

// Helper component for the status filter
const StatusFilter = ({
  column,
}: {
  column: Column<UFCEvent, unknown>
}) => {
  const statuses = ["UPCOMING", "COMPLETED", "CANCELLED"]

  return (
    <DropdownMenu>
      <DropdownMenuTrigger asChild>
        <Button variant="ghost" className="h-8 w-8 p-0">
          <span className="sr-only">Filter status</span>
          <Filter className="h-4 w-4" />
        </Button>
      </DropdownMenuTrigger>
      <DropdownMenuContent align="end">
        <DropdownMenuLabel>Status</DropdownMenuLabel>
        {statuses.map((status) => (
          <DropdownMenuCheckboxItem
            key={status}
            checked={(column.getFilterValue() as string[])?.includes(status)}
            onCheckedChange={(checked) => {
              const currentFilter = (column.getFilterValue() as string[]) ?? []
              if (checked) {
                column.setFilterValue([...currentFilter, status])
              } else {
                column.setFilterValue(
                  currentFilter.filter((item) => item !== status)
                )
              }
            }}
          >
            {status}
          </DropdownMenuCheckboxItem>
        ))}
      </DropdownMenuContent>
    </DropdownMenu>
  )
}

